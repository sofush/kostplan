package com.example.kostplan.entity;

/**
 * Describes a person's body weight goal.
 */
public enum WeightGoal {
	/**
	 * A desire to gain body weight.
	 */
	GAIN,

	/**
	 * A desire to lose body weight.
	 */
	LOSS,

	/**
	 * A desire to maintain one's current body weight.
	 */
	EQUILIBRIUM,

	/**
	 * A desire to build muscle.
	 */
	MUSCLE,
}
